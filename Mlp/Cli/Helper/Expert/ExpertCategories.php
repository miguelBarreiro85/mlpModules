    public static function setExpertCategories($categories,$logger,$sku) {
            print_r($categories);
            $pieces = explode("->",$categories);
            print_r($pieces);
            $gama = $pieces[0];
            $familia = $pieces[1];
            $subFamilia = $pieces[2];
            switch($gama) {
                //Informática->Monitores->LFD
                case 'Impressoras':
                case 'Informática':
                    $gama = self::INFORMATICA;
                    switch ($familia) {
                        case 'Computadores e Tablets':
                            $familia = self:COMPUTADORES_E_TABLETS;
                            switch ($subFamilia) {
                                case 'Notebooks':
                                    $subFamilia = self::PORTATEIS_NOTEBOOKS;
                                    return [$gama,$familia,$subFamilia];
                                case 'Tablets':
                                    $subFamilia = self::TABLETS;
                                    return [$gama,$familia,$subFamilia];
                                case 'Acessórios Tablets':
                                    $subFamilia = self::ACESSORIOS_TABLETS;
                                    return [$gama,$familia,$subFamilia];
                                case 'Acessórios Notebooks':
                                    $subFamilia = self::ACESSORIOS_NOTEBOOKS;
                                    return [$gama,$familia,$subFamilia];
                                case 'Outros':
                                    $subFamilia = self::ACESSORIOS_NOTEBOOKS;
                                    $logger->info(self::VERIFICAR_SUBFAMILIA.$sku);
                                default:
                                    return [$gama,$familia,$subFamilia];
                            }
                        case 'Monitores':
                            $familia = self::MONITORES;
                            switch ($subFamilia) {
                                case 'LFD':
                                case 'TFT':
                                case 'LED':
                                    $subFamilia = self::MONITORES_PC;
                                    return [$gama,$familia,$subFamilia];
                                case 'Curvo':
                                    $subFamilia = self::MONITORES_PC_CURVO;
                                    return [$gama,$familia,$subFamilia];
                                case 'LED TV':
                                    $subFamilia = self::MONITORES_C_TV;
                                    return [$gama,$familia,$subFamilia];
                                case 'Tácteis':
                                    $subFamilia = self::MONITORES_TACTEIS;
                                    return [$gama,$familia,$subFamilia];
                                default:
                                    return [$gama,$familia,$subFamilia];
                            }
                            break;
                        case 'Impressoras':
                            //Informática->Impressoras->Jacto de Tinta
                            switch ($subFamilia) {
                                case 'Acessórios':
                                case 'Consumíveis':
                                    $subFamilia = self::ACESSORIOS_IMPRESSORAS;
                                    return [$gama,$familia,$subFamilia];
                                case 'Jacto de Tinta':
                                    $familia = self::IMPRESSORAS;
                                    $subFamilia = self::IMPRESSORAS_JACTO_DE_TINTA;
                                    return [$gama,$familia,$subFamilia];
                                default:
                                    return [$gama,$familia,$subFamilia];
                                    
                            }
                        case 'Jacto de Tinta':
                            $familia = self::IMPRESSORAS;
                            $subFamilia = self::IMPRESSORAS_JACTO_DE_TINTA;
                            return [$gama,$familia,$subFamilia];
                        case 'Integração e Periféricos':
                            $familia = self::ACESSORIOS_INFORMATICA;
                            switch ($subFamilia) {
                                case 'UPS':
                                    $subFamilia = self::UPS;
                                    return [$gama,$familia,$subFamilia];
                                case 'Webcams':
                                    $subFamilia = self::WEB_CAMS;
                                    return [$gama,$familia,$subFamilia];
                                case 'Armazenamento Dados':
                                    $familia = self::MEMORIAS;
                                    $subFamilia = null;
                                    $logger->info(self::VERIFICAR_SUBFAMILIA.$sku);
                                    return [$gama,$familia,$subFamilia];
                                case 'Leitor de cartões':
                                    $subFamilia = self::LEITOR_CARTOES;
                                    return [$gama,$familia,$subFamilia];
                                case 'Colunas':
                                    $gama = self::IMAGEM_E_SOM
                                    $familia = self::COLUNAS;
                                    $subFamilia = null;
                                    return [$gama,$familia,$subFamilia];
                                case 'Outros':
                                case 'Periféricos':
                                case 'Teclados e Ratos':
                                case 'drives':
                                case 'Hubs USB':
                                    $subFamilia::OUTROS_ACESSORIOS_INFORMATICA;
                                    $logger->info(self::VERIFICAR_SUBFAMILIA.$sku);
                                    return [$gama,$familia,$subFamilia];
                                case 'Adaptadores e Cabos':
                                case 'Redes':
                                    $subFamilia = self::REDES_CABOS;
                                    $logger->info(self::VERIFICAR_SUBFAMILIA.$sku);
                                    return [$gama,$familia,$subFamilia];
                                case 'Transporte':
                                    $subFamilia = self::MALAS_BOLSAS_INFORMATICA;
                                    return [$gama,$familia,$subFamilia];
                                

                                default:
                                    return [$gama,$familia,$subFamilia];
                                    break;
                            }
                        case 'Software':
                            $familia = self::INFORMATICA_SOFTWARE;
                            switch ($subFamilia) {
                                case 'Segurança':
                                    $subFamilia = self::INFORMATICA_SOFTWARE_SEGURANCA;
                                    return [$gama,$familia,$subFamilia];
                                case 'POS':
                                    $subFamilia = self::INFORMATICA_SOFTWARE_POS;
                                    return [$gama,$familia,$subFamilia];
                                default:
                                    return [$gama,$familia,$subFamilia];
                                    break;
                            }
                        case 'Gaming':
                            $familia = self::GAMING;
                            switch ($subFamilia) {
                                case 'Acessórios Playstation':
                                    $subFamilia = self::ACESSORIOS_PLAYSTATION;
                                    return [$gama, $familia, $subFamilia];
                                case 'Consolas Playstation':
                                    $subFamilia = self::CONSOLAS_PLAYSTATION;
                                    return [$gama, $familia, $subFamilia];
                                case 'Jogos Playstation':
                                    $subFamilia = self::JOGOS_PLAYSTATION;
                                    return [$gama, $familia, $subFamilia];
                                case 'Acessórios PC Gaming':
                                    $subFamilia = self::ACESSORIOS_PC_GAMING;
                                    return [$gama, $familia, $subFamilia];
                                default:
                                    return [$gama, $familia, $subFamilia];
                            }
                        default:
                            return [$gama,$familia,$subFamilia];
                    }
                
                case 'Audiovisual':
                    $gama = self::IMAGEM_E_SOM;
                    switch ($familia) {
                        case 'TV':
                            switch ($subFamilia) {
                                case 'Suportes':
                                    $familia = self::ACESSORIOS_IMAGEM_E_SOM;
                                    $subFamilia = self::MOVEIS_SUPORTES;
                                    return [$gama,$familia,$subFamilia];
                                case 'Acessórios':
                                    $familia = self::ACESSORIOS_IMAGEM_E_SOM;
                                    $subFamilia = self::ACESSORIOS_TV;
                                    return [$gama,$familia,$subFamilia];
                                case 'Cabos':    
                                    $familia = self::ACESSORIOS_IMAGEM_E_SOM;
                                    $subFamilia = self::CABOS_TV;
                                    return [$gama,$familia,$subFamilia];
                            
                                case 'De 50 a 65 Polegadas':
                                    $familia = self::TELEVISAO;
                                    $subFamilia = self::TV_LED_50_60;
                                    return [$gama,$familia,$subFamilia];
                                case 'Até 49 Polegadas':
                                    $familia = self::TELEVISAO;
                                    $subFamilia = self::TV_LED_M42;
